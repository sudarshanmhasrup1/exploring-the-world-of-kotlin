package com.trying.kotlin.variables

fun main() {
    // String data type
    val fullName = "Sudarshan Mhasrup"

    // Integer data type
    val age = 21

    // Boolean data type
    val isDeveloper = true

    // Character data type
    val division = 'B'

    // Double data type
    val cgpa = 7.31

    // Float data type
    val attendance = 78.20F

    println("Name: $fullName.")
    println("Age: $age years.")
    println("Class/Division: $division.")
    println("CGPA: $cgpa.")
    println("Attendance: $attendance%.")
    println("Is developer: $isDeveloper.")
}