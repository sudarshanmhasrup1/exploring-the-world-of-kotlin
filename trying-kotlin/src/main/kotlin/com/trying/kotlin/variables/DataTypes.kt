package com.trying.kotlin.variables

fun main() {
    // String data type
    val fullName = "Sudarshan Mhasrup"

    // Integer data type
    val age = 21

    // Boolean data type
    val isDeveloper = true

    // Character data type
    val division = 'B'

    // Double data type
    val cgpa = 7.31

    // Float data type
    val attendance = 78.20F

    println(message = "Name: $fullName.")
    println(message = "Age: $age years.")
    println(message = "Class/Division: $division.")
    println(message = "CGPA: $cgpa.")
    println(message = "Attendance: $attendance%.")
    println(message = "Is developer: $isDeveloper.")
}